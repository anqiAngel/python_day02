import matplotlib

# 运算符可以对一个值或多个值进行运算或各种操作
# 运算符分类:1.算术运算符 2.赋值运算符 = 3.比较运算符(关系运算符) 4.逻辑运算符 5.条件运算符(三元运算符)
# 算术运算符:+、-、*、/ 、//(整除)、2**2(2的2次幂)、% 取模取余数

a = 10 + 5
b = 'hello' + ' ' + 'world'
# 求和
print(a)
# 拼串
print(b)
c = 10 - 5
# 相减
print(c)
d = 5 * 5
# 复制字符串
e = 'hello' * 10
# 相乘
print(d)
# 复制字符串
print(e)
f = 10 / 5
# 相除 总会返回浮点数
print(f)
g = 10//3
# 整除 总会返回整数
print(g)
# 10的10次幂
h = 10 ** 10
# 16开方
i = 16 ** 0.5
print(h)
print(i)
# 取模、取余
j = 10 % 8
print(j)


